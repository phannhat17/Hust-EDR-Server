interface OverviewProps {
  chartData?: {
    date: string;
    count: number;
  }[];
}

export function Overview({ chartData }: OverviewProps) {
  // Transform the incoming data to the format expected by the chart
  const transformedData = chartData
    ? chartData.map(item => ({
        name: new Date(item.date).toLocaleDateString(undefined, { month: 'short', day: 'numeric' }),
        total: item.count,
      }))
    : defaultData;

  return (
    <ResponsiveContainer width='100%' height={350}>
      <BarChart data={transformedData}>
        <XAxis
          dataKey='name'
          stroke='#888888'
          fontSize={12}
          tickLine={false}
          axisLine={false}
        />
        <YAxis
          stroke='#888888'
          fontSize={12}
          tickLine={false}
          axisLine={false}
          tickFormatter={(value) => `${value}`}
        />
        <Bar
          dataKey='total'
          fill='currentColor'
          radius={[4, 4, 0, 0]}
          className='fill-primary'
        />
      </BarChart>
    </ResponsiveContainer>
  )
}
